// ─── GSAP 3D TILT WRAPPER COMPONENT ──────────────────────────────────────
function Tilt3D({ children, className, intensity = 14 }) {
  const containerRef = useRef(null);
  const glareRef = useRef(null);

  const handleMouseMove = (e) => {
    if (!containerRef.current || !glareRef.current) return;
    const card = containerRef.current;
    const box = card.getBoundingClientRect();

    const x = e.clientX - box.left;
    const y = e.clientY - box.top;

    const normX = (x - box.width / 2) / box.width;
    const normY = (y - box.height / 2) / box.height;

    const rotX = -normY * intensity;
    const rotY = normX * intensity;

    gsap.to(card, {
      rotateX: rotX,
      rotateY: rotY,
      scale: 1.02,
      transformPerspective: 1000,
      duration: 0.35,
      ease: 'power2.out',
      overwrite: 'auto',
    });

    const glareX = (x / box.width) * 100;
    const glareY = (y / box.height) * 100;
    gsap.to(glareRef.current, {
      opacity: 0.15,
      background: `radial-gradient(circle at ${glareX}% ${glareY}%, rgba(255,255,255,0.85) 0%, rgba(255,255,255,0) 75%)`,
      duration: 0.25,
      overwrite: 'auto',
    });
  };

  const handleMouseLeave = () => {
    if (!containerRef.current || !glareRef.current) return;
    gsap.to(containerRef.current, {
      rotateX: 0,
      rotateY: 0,
      scale: 1,
      duration: 0.6,
      ease: 'power2.out',
      overwrite: 'auto',
    });
    gsap.to(glareRef.current, {
      opacity: 0,
      duration: 0.5,
      overwrite: 'auto',
    });
  };

  return (
    <div
      ref={containerRef}
      onMouseMove={handleMouseMove}
      onMouseLeave={handleMouseLeave}
      style={{ transformStyle: 'preserve-3d' }}
      className={`relative rounded-2xl overflow-hidden ${className || ''}`}
    >
      <div
        ref={glareRef}
        className="absolute inset-0 pointer-events-none transition-opacity duration-200 z-50 opacity-0"
      />
      <div style={{ transform: 'translateZ(0px)', transformStyle: 'preserve-3d' }}>
        {children}
      </div>
    </div>
  );
}

