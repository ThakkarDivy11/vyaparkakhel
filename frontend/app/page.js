// ─── GSAP ANIMATED WIN-RATE progress circle ──────────────────────────────
function WinRateCircle({ rate }) {
  const circleRef = useRef(null);
  const circumference = 2 * Math.PI * 24;

  useEffect(() => {
    if (!circleRef.current) return;
    const offset = circumference - (rate / 100) * circumference;

    gsap.fromTo(circleRef.current,
      { strokeDashoffset: circumference },
      {
        strokeDashoffset: offset,
        duration: 1.4,
        ease: 'power3.out',
        delay: 0.15
      }
    );
  }, [rate, circumference]);

  return (
    <Card variant="default" className="p-4 flex items-center gap-4 bg-surface border border-border shadow-xs">
      <div className="relative w-14 h-14 flex items-center justify-center shrink-0">
        <svg className="w-full h-full transform -rotate-90">
          <circle cx="28" cy="28" r="24" className="stroke-surface-2 fill-none" strokeWidth="4" />
          <circle
            ref={circleRef}
            cx="28"
            cy="28"
            r="24"
            className="stroke-portage-500 fill-none"
            strokeWidth="4"
            strokeDasharray={circumference}
            strokeLinecap="round"
          />
        </svg>
        <span className="absolute font-mono font-black text-xs text-text">{rate}%</span>
      </div>
      <div>
        <h5 className="font-extrabold text-xs text-text-muted uppercase tracking-wider leading-none">Win Ratio</h5>
        <p className="text-[10px] text-text-muted mt-1 leading-tight">Percentage of multiplayer rooms won.</p>
      </div>
    </Card>
  );
}

